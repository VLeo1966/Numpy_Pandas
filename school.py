import pandas as pd
import numpy as np

# Шаг 1: Создание DataFrame с данными
data = {
    'Имя': ['Алексей', 'Борис', 'Виктория', 'Галина', 'Дмитрий', 'Екатерина', 'Жанна', 'Игорь', 'Ксения', 'Лев'],
    'Математика': [85, 78, 92, 88, 90, 95, 72, 83, 89, 76],
    'Физика': [80, 75, 91, 85, 88, 94, 70, 82, 87, 73],
    'Химия': [78, 74, 89, 83, 87, 93, 69, 81, 85, 72],
    'Биология': [82, 76, 90, 84, 89, 94, 71, 84, 88, 75],
    'Литература': [87, 79, 93, 86, 91, 96, 73, 85, 90, 77]
}

df = pd.DataFrame(data)

# Шаг 2: Вывод первых нескольких строк DataFrame
print("Первые строки DataFrame:")
print(df.head(), '\n')

# Шаг 3: Вычисление средней оценки по каждому предмету
average_scores = df.mean(numeric_only=True)
print("Средняя оценка по каждому предмету:")
print(average_scores, '\n')

# Шаг 4: Вычисление медианной оценки по каждому предмету
median_scores = df.median(numeric_only=True)
print("Медианная оценка по каждому предмету:")
print(median_scores, '\n')

# Шаг 5: Вычисление Q1 и Q3 для оценок по математике
q1_math = df['Математика'].quantile(0.25)
q3_math = df['Математика'].quantile(0.75)
iqr_math = q3_math - q1_math

print(f"Q1 для Математики: {q1_math}")
print(f"Q3 для Математики: {q3_math}")
print(f"IQR для Математики: {iqr_math}\n")

# Шаг 6: Вычисление стандартного отклонения для каждого предмета
std_deviation = df.std(numeric_only=True)
print("Стандартное отклонение по каждому предмету:")
print(std_deviation)
